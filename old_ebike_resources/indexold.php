                    function setProductToSlot(slot, product) {
                        if (product == null) {
                            slot.div.style.display = "none";
                            slot.input.checked = false;
                            return;
                        }
                        else {
                            slot.div.style.display = "inline";
                            slot.input.disabled = false;
                        }

                        slot.product = product;
                        slot.image.src = product.image;
                        slot.name.innerHTML = product.name;
                        if (slot.price != null)
                            slot.price.innerHTML = formatPriceString(product.price);
                        slot.name.setAttribute('href', product.url);

                        // Motor?
                        if (product.maxPower != null) {
                            slot.param0_value.innerHTML = product.maxPower;
                            slot.param1_value.innerHTML = product.mass;

                            var variantStockRim = null;
                            var variantCustomRim = null;
                            for (var i = 0; i < product.variants.length; i++) {
                                var variant = product.variants[i];
                                if (variant.isRear == desiredIsRear && variant.size == desiredRim) {
                                    if (variant.isCustomRim) {
                                        variantCustomRim = variant;
                                    } else {
                                        variantStockRim = variant;
                                    }
                                }
                            }

                            var variant = (variantStockRim != null && variantStockRim.isAvailable) ? variantStockRim :
                                (variantCustomRim != null && variantCustomRim.isAvailable ? variantCustomRim : null);

                            if (variant != null) {
                                slot.variant = variant;
                                if (slot.price != null)
                                    slot.price.innerHTML = formatPriceString(variant.price);
                            } else {
                                slot.variant = null;
                                slot.input.disabled = true;
                                slot.price.innerHTML = "Нет в наличии";
                            }
                        }
                        // Controller?
                        else if (product.current != null) {
                            slot.param0_value.innerHTML = product.current;
                        }
                        // Battery?
                        else if (product.cell != null) {
                            slot.param0_value.innerHTML = product.voltage;
                            slot.param1_value.innerHTML = product.ah;
                            slot.param2_value.innerHTML = product.mass;
                        }

                        if (product.isAvailable == false) {
                            slot.variant = null;
                            slot.input.disabled = true;
                            if (slot.price != null) {
                                slot.price.innerHTML = "Нет в наличии";
                            }
                        }

                        setSlotTooltip(slot, product.tooltip);
                    }

