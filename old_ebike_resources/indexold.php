                    function createVirtualBattery(s, cell, current, maxCurrent, capacity) {
                        var minPForController = Math.ceil(maxCurrent / cell.currentMax);	// Battery must allow controller's max current
                        var minPForSpeed = Math.ceil(current / cell.current);	// Battery must allow this constant current
                        var minPForRange = Math.ceil(capacity / s / (cell.capacity * 3.6));
                        var p = Math.max(minPForSpeed, Math.max(minPForController, minPForRange));

                        var minPForConstantFullPower = Math.ceil(maxCurrent / cell.current);

                        // Lets try to match to stock battery

                        if (cell.codeName == "Panasonic" && s == 13) {
                            if (p <= 3)
                                p = 3;
                            else if (p <= 5)
                                p = 5;
                        }
                        if (cell.codeName == "White" && s == 13) {
                            if (p <= 3)
                                p = 3;
                            else if (p <= 6)
                                p = 6;
                        }

                        var cellMaxCurrent = cell.currentMax * p;

                        var voltage = Math.floor(3.7 * s);
                        var ah = Math.round10(cell.capacity * p, -1);
                        var wh = Math.round(cell.capacity * 3.6 * s * p);
                        var mass = Math.round10(0.05 * s * p + 0.5, -1); // todo
                        var bms = {};

                        for (var i = 0; i < products.allBmses.length; i++) {
                            if (products.allBmses[i].cells >= s && products.allBmses[i].maxCurrent >= cellMaxCurrent /*products.allBmses[i].maxCurrent >= current*/) {
                                bms = products.allBmses[i];
                                break;
                            }
                        }

                        var price = s * p * (cell.price);
                        price += Math.max(1500, s * p * 55);
                        if (bms != null)
                            price += bms.price;

                        var chargerPrice = 4200;
                        var chargeCurrent = 5;
                        if (s >= 21)
                            chargeCurrent = 2;
                        // Заменим на мелкий
                        if (s <= 14 && ah <= 12) {
                            chargerPrice = 2000;
                            chargeCurrent = 2;
                        }
                        price += chargerPrice;

                        var battery = {
                            name: cell.name + " " + voltage + "/" + ah,
                            image: "https://static-eu.insales.ru/images/products/1/3871/92557087/medium_DSC_3992.jpg",
                            s: s,
                            p: p,
                            cell: cell,
                            bms: bms,
                            voltage: voltage,
                            ah: ah,
                            wh: wh,
                            mass: mass,
                            price: price,
                            url: "https://e4bike.ru/page/battery-configurator",
                            chargeCurrent: chargeCurrent,
                            goodForConstantFullPower: p >= minPForConstantFullPower
                        };

                        // Lets try to replace with a stock battery
                        var stockReplacement = null;
                        if (battery.cell.codeName == "Panasonic") {
                            if (battery.s == 13 && battery.p == 3) {
                                stockReplacement = products.batteries.panasonic_13s_3p;
                            } else if (battery.s == 13 && battery.p == 5) {
                                stockReplacement = products.batteries.panasonic_13s_5p;
                            }
                        } else if (battery.cell.codeName == "White") {
                            if (battery.s == 13 && battery.p == 3) {
                                stockReplacement = products.batteries.white_13s_3p;
                            } else if (battery.s == 13 && battery.p == 6) {
                                stockReplacement = products.batteries.white_13s_6p;
                            }
                        }

                        if (stockReplacement != null) {
                            battery.url = stockReplacement.url;
                            battery.image = stockReplacement.image;
                            battery.price = stockReplacement.price + chargerPrice;
                        }

                        return battery;
                    }

                    function addToCart() {
                        productIds = [];
                        var motorAvailable = false;

                        // Motor
                        if (selection.motorVariant != null) {
                            productIds.push({id: selection.motorVariant.id, count: 1, comment: ""});
                            motorAvailable = true;
                        }

                        document.getElementById('divBuyButtonWarning').style.display = motorAvailable ? 'none' : 'inline';
                        if (!motorAvailable)
                            return;

                        // Conroller
                        productIds.push({
                            id: selection.controller.id,
                            count: 1,
                            comment: "Ручка - " + selection.throttle.name
                        });

                        // Battery
                        var closestBatteryVariant = products.customBatteryVariants[0];
                        for (var i = 0; i < products.customBatteryVariants.length; ++i) {
                            var variant = products.customBatteryVariants[i];
                            var priceDelta = Math.abs(selection.battery.price - variant.price);
                            var bestPriceDelta = Math.abs(selection.battery.price - closestBatteryVariant.price)

                            if (priceDelta < bestPriceDelta)
                                closestBatteryVariant = variant;
                        }

                        var batteryComment = selection.battery.cell.codeName + ", " + selection.battery.s + "s " + selection.battery.p + "p" +
                            ", BMS: " + selection.battery.bms["name"] +
                            ", зарядка: " + selection.battery.chargeCurrent + " A, сборка прямоугольник";

                        productIds.push({id: closestBatteryVariant.id, count: 1, comment: batteryComment});

                        // Accessories
                        for (var i = 0; i < selection.accessories.length; ++i) {
                            var acc = selection.accessories[i];
                            productIds.push({id: acc.id, count: acc.count, comment: ""});
                        }

                        cartAddIndex = 0;
                        sumbitAndCheckout(productIds);

                        amplitude.getInstance().logEvent('CONFIGURATOR_ADD_TO_CART');
                    }

                    function sumbitAndCheckout() {
                        $("#btnAddToCart").css("display", "none");
                        $("#imageAddToCartProgress").css("display", "inline");

                        if (cartAddIndex < productIds.length) {
                            $.post("/cart_items", {
                                variant_id: productIds[cartAddIndex].id,
                                quantity: productIds[cartAddIndex].count,
                                comment: productIds[cartAddIndex].comment
                            }, function (data) {
                                sumbitAndCheckout();
                            });
                            cartAddIndex++;
                        }
                        else {
                            window.location.href = "../cart_items.html";
                        }
                    }

